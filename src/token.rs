#[derive(Debug)]
pub struct Span {
    pub start: u32,
    pub end: u32,
}

#[derive(Debug)]
pub struct Token {
    pub span: Span,
    pub kind: TokenKind,
}

#[derive(Copy, Clone, PartialEq, Debug)]
pub enum TokenKind {
    Ident,
    // Literals
    LitInt(u64),
    LitFloat(f64),
    LitBool(bool),
    LitString,
    // Special
    Error,
    Eof,
    // Keywords
    KwPub,
    KwMod,
    KwMut,
    KwSelf,
    KwImpl,
    KwEnum,
    KwStruct,
    KwImport,
    // Stmt
    KwIf,
    KwElse,
    KwFor,
    KwDefer,
    KwBreak,
    KwReturn,
    KwSwitch,
    KwContinue,
    // Expr
    KwCast,
    KwSizeof,
    KwTrue,
    KwFalse,
    // Type
    KwS8,
    KwS16,
    KwS32,
    KwS64,
    KwU8,
    KwU16,
    KwU32,
    KwU64,
    KwF32,
    KwF64,
    KwBool,
    KwString,
    // Delimeter
    OpenParen,
    OpenBlock,
    OpenBracket,
    CloseParen,
    CloseBlock,
    CloseBracket,
    // Separator
    At,
    Dot,
    Colon,
    Comma,
    Semicolon,
    DotDot,
    ColonColon,
    ArrowThin,
    ArrowWide,
    /// Unary op
    LogicNot,
    BitNot,
    // Binary cmp
    LogicAnd,
    LogicOr,
    Less,
    Greater,
    LessEq,
    GreaterEq,
    IsEq,
    NotEq,
    // Binary op
    Plus,
    Minus,
    Times,
    Div,
    Mod,
    BitAnd,
    BitOr,
    BitXor,
    Shl,
    Shr,
    // Assign op
    Assign,
    PlusEq,
    MinusEq,
    TimesEq,
    DivEq,
    ModEq,
    BitAndEq,
    BitOrEq,
    BitXorEq,
    ShlEq,
    ShrEq,
}

impl Token {
    pub fn new(start: u32, end: u32, kind: TokenKind) -> Self {
        Self {
            span: Span { start, end },
            kind,
        }
    }

    pub fn eof() -> Self {
        Self::new(0, 0, TokenKind::Eof)
    }
}

